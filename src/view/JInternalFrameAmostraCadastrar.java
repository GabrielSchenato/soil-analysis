    public JInternalFrameAmostraCadastrar(JDesktopPane jp, Connection conn) {
        initComponents();
        this.JDesktop = jp;
        conexao = conn;
        DefaultTableModel x = (DefaultTableModel) jTable1.getModel();
        this.modelo = x;
        TrocaTabPorEnter(this);
        PreparedStatement pstmt = null;
        try {
            DefaultComboBoxModel defaultComboBox = new DefaultComboBoxModel();
            pstmt = conexao.prepareStatement("SELECT * FROM tipos_amostras");
            ResultSet rs = pstmt.executeQuery();
            defaultComboBox.addElement("Selecione um tipo de amostra");
            while (rs.next()) {
                defaultComboBox.addElement(new TiposAmostras(rs.getInt("idTipoAmostra"), rs.getString("nome"), rs.getString("descricao")));
            }
            pstmt.close();
            
            cbTipoAnalise.setModel(defaultComboBox);
        } catch (SQLException ex) {
            Logger.getLogger(JInternalFrameAmostraCadastrar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    static void TrocaTabPorEnter(Container frame) {
        HashSet conj = new HashSet(frame.getFocusTraversalKeys(KeyboardFocusManager.FORWARD_TRAVERSAL_KEYS));
        conj.add(AWTKeyStroke.getAWTKeyStroke(KeyEvent.VK_ENTER, 0));
        frame.setFocusTraversalKeys(KeyboardFocusManager.FORWARD_TRAVERSAL_KEYS, conj);
    }

