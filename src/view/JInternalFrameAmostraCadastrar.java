    private void btnBuscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarClienteActionPerformed
        if (!buscarCliente.getText().isEmpty()) {
            PreparedStatement pstmt = null;
            String opcao1 = "%";
            String opcao2 = "%";
            try {
                pstmt = conexao.prepareStatement("Select * From clientes Where nome like ?");
                pstmt.setString(1, opcao1 + buscarCliente.getText() + opcao2);
                ResultSet rs = pstmt.executeQuery();
                boolean resultado = true;
                while (rs.next()) {
                    int id = rs.getInt("idCliente");
                    idCliente.setText(Integer.toString(id));
                    nomeCliente.setText(rs.getString("nome"));
                    telefoneCliente.setText(rs.getString("telefone"));
                    ruaCliente.setText(rs.getString("rua"));
                    bairroCliente.setText(rs.getString("bairro"));
                    cepCliente.setText(rs.getString("cep"));
                    cidadeUFCliente.setText(rs.getString("cidade") + "/" + rs.getString("uf"));
                    resultado = false;
                }
                if (resultado) {
                    JOptionPane.showMessageDialog(null, "Nenhum resultado encontrado! ");
                }
                pstmt.close();
            } catch (SQLException ex) {
                Logger.getLogger(JInternalFrameAmostraCadastrar.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Preencha o campo de pesquisa! ");
        }
    }//GEN-LAST:event_btnBuscarClienteActionPerformed

