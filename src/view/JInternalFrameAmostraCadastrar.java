    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
        int qtdaamostras = 0;
        PreparedStatement pstmt = null;
        if ((idCliente.getText().isEmpty()) || (idSolicitante.getText().isEmpty()) || (cbTipoAnalise.getSelectedIndex() == 0)) {
            JOptionPane.showMessageDialog(null, "Os campos não podem retornar vazios");
        } else {
            for (int i = 0; i < jTable1.getModel().getRowCount(); i++) {
                if (modelo.getValueAt(i, 0) == null) {
                    JOptionPane.showMessageDialog(null, "O campo area não pode ser nulo!");
                } else {
                    try {
                        pstmt = conexao.prepareStatement("insert into amostras (idTipoAmostra, idCliente, idSolicitante, area, complemento) values (?,?,?,?,?)");
                        TiposAmostras x = (TiposAmostras) cbTipoAnalise.getSelectedItem();
                        String idTipoAmostra = Integer.toString(x.getidTipoAmostra());
                        pstmt.setString(1, idTipoAmostra);
                        pstmt.setString(2, idCliente.getText());
                        pstmt.setString(3, idSolicitante.getText());
                        pstmt.setString(4, (String) modelo.getValueAt(i, 0));
                        pstmt.setString(5, (String) modelo.getValueAt(i, 1));
                        pstmt.execute();
                        pstmt.close();
                        qtdaamostras++;
                    } catch (SQLException ex) {
                        Logger.getLogger(JInternalFrameClienteCadastrar.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }
        if (qtdaamostras > 0) {
            JOptionPane.showMessageDialog(null, "Número de amostras cadastradas com sucesso: " + qtdaamostras);
            reset();
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar as amostras, tente novamente! ");
        }
    }//GEN-LAST:event_salvarActionPerformed

